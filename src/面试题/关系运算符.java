public class 关系运算符 {
    public static void main(String[] args) {
        /**
         * 等号两边的数据类型必须一致，可相互转换的基本类型除外
         */
        int a=1;
        Integer b=1;
        long c=1;
        Long d=1l;
        System.out.println(a==b);
        System.out.println(a==c);
        System.out.println(a==d);
    }
}
